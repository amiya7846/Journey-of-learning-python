a = 1  # a is an integer data type

b = 3.78 # b is a float data type

c = "Amiya" # c is a string data type

d = True # d is a boolean data type

e = None # e is a NoneType data type

# We can determine the types by using the function called'type()'

print("Value of a:", a)  # Output: 1
print("Type of a:", type(a)) # Output: <class 'int'>

print("Value of b:", b)  # Output: 3.78
print("Type of b:", type(b)) # Output: <class 'float'>

print("Value of c:", c)  # Output: Amiya
print("Type of c:", type(c)) # Output: <class 'str'>

print("Value of d:", d)  # Output: True
print("Type of d:", type(d)) # Output: <class 'bool'>

print("Value of e:", e)  # Output: None
print("Type of e:", type(e)) # Output: <class 'NoneType'>

